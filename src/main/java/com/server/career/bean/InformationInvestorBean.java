package com.server.career.bean;

public class InformationInvestorBean {
	public int investorId;
	public String investorImage;
	public String investorDescription;
	public Integer inforId;

	public int getInvestorId() {
		return investorId;
	}

	public void setInvestorId(int investorId) {
		this.investorId = investorId;
	}

	public String getInvestorImage() {
		return investorImage;
	}

	public void setInvestorImage(String investorImage) {
		this.investorImage = investorImage;
	}

	public String getInvestorDescription() {
		return investorDescription;
	}

	public void setInvestorDescription(String investorDescription) {
		this.investorDescription = investorDescription;
	}

	public Integer getInforId() {
		return inforId;
	}

	public void setInforId(Integer inforId) {
		this.inforId = inforId;
	}

}
